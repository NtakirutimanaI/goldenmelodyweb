          <h2>Testimony to a Sensitive Choir Director</h2>
          <p>I was shown the case of Brother S., that he would be a burden to the church unless he comes into a closer relation with God. He is self-conceited. If his course is questioned he feels hurt. If he thinks another is preferred before him, he feels that it is an injury done to him</p>>. . .

<p>Brother S. has a good knowledge of music, but his education in music was of a character to suit the stage rather than the solemn worship of God. Singing is just as much the worship of God in a religious meeting as speaking, and any oddity or peculiarity cultivated attracts the attention of the people and destroys the serious, solemn impression which should be the result of sacred music. Anything strange and eccentric in singing detracts from the seriousness and sacredness of religious service.</p>

<p>Bodily exercise profiteth little. Everything that is connected in any way with religious worship should be dignified, solemn, and impressive. God is not pleased when ministers professing to be Christ's representatives so misrepresent Christ as to throw the body into acting attitudes, making undignified and coarse gestures, unrefined, coarse gesticulations. All this amuses, and will excite the curiosity of those who wish to see strange, odd, and exciting things, but these things will not elevate the minds and hearts of those who witness them.</p>

<p>The very same may be said of singing. You assume undignified attitudes. You put in all the power and volume of the voice you can. You drown the finer strains and notes of voices more musical than your own. This bodily exercise and the harsh, loud voice makes no melody to those who hear on earth and those who listen in heaven. This singing is defective and not acceptable to God as perfect, softened, sweet strains of music. There are no such exhibitions among the angels as I have sometimes seen in our meetings. Such harsh notes and gesticulations are not exhibited among the angel choir. Their singing does not grate upon the ear. It is soft and melodious and comes without this great effort I have witnessed. It is not forced and strained, requiring physical exercise.</p>

<p>Brother S.is not aware how many are amused and disgusted. Some cannot repress thoughts not very sacred and feelings of levity to see the unrefined motions made in the singing. Brother S., exhibits himself. His singing does not have an influence to subdue the heart and touch the feelings. Many have attended the meetings and listened to the words of truth spoken from the pulpit, which have convicted and solemnized their minds; but many times the way the singing has been conducted has not deepened the impression made. The demonstrations and bodily contortions, the unpleasant appearance of the strained, forced effort has appeared so out of place for the house of God, so comical, that the serious impressions made upon the minds have been removed.Those who believe the truth are not as highly thought of as before the singing.</p>

<p>Brother S.'s case has been a difficult one to manage. He has been like a child undisciplined and uneducated. When his course has been questioned, instead of taking reproof as a blessing, he has let his feelings get the better of his judgment and he has become discouraged and would do nothing. If he could not do in everything as he wanted to do, all in his way, he would not help at all. He has not taken hold of the work earnestly to reform his manners but has given up to mulish feelings that separate the angel from him and bring evil angels around him. The truth of God received in the heart commences its refining, sanctifying influence upon the life.</p>

<p>Brother S. has thought that singing was about the greatest thing to be done in this world and that he had a very large and grand way of doing it. Your singing is far from pleasing to the angel choir. Imagine yourself standing in the angel band elevating your shoulders, emphasizing the words, motioning your body and putting in the full volume of your voice. What kind of concert and harmony would there be with such an exhibition before the angels?</p>

<p>Music is of heavenly origin. There is great power in music. It was music from the angelic throng that thrilled the hearts of the shepherds on Bethlehem's plains and swept round the world. It is in music that our praises rise to Him who is the embodiment of purity and harmony. It is with music and songs of victory that the redeemed shall finally enter upon the immortal reward.</p>

<p>There is something peculiarly sacred in the human voice. Its harmony and its subdued and heaven-inspired pathos exceeds every musical instrument. Vocal music is one of God's gifts to men, an instrument that cannot be surpassed or equaled when God's love abounds in the soul. Singing with the spirit and the understanding also is a great addition to devotional services in the house of God.</p>

<p>How this gift has been debased! When sanctified and refined it would accomplish great good in breaking down the barriers of prejudice and hardhearted unbelief, and would be the means of converting souls. It is not enough to understand the rudiments of singing, but with the knowledge, must be such a connection with heaven that angels can sing through us.</p>

<p>Your voice has been heard in church so loud, so harsh, accompanied or set off with your gesticulations not the most graceful, that the softer and more silvery strains, more like angel music, could not be heard. You have sung more to men than to God. As your voice has been elevated in loud strains above all the congregation, you have been thoughtful of the admiration 
you were exciting. You have really had such high ideas of your singing, that you have had some thoughts that you should be remunerated for the exercise of this gift.</p>

<p>The love of praise has been the mainspring of your life. This is a poor motive for a Christian. You have wanted to be petted and praised like a child. You have had much to contend with in your own nature. It has been hard work for you to overcome your natural besetments and live a self-denying, holy life. --Manuscript 5, 1874.</p>


